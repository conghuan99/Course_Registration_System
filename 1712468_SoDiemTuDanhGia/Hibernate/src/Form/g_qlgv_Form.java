    private void jTextField6KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyPressed
        String userName= "";
        Giaovu gv= new Giaovu();
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            for(Giaovu gv1 : GiaovuDAO.layDanhSachGiaoVu()){
                if(gv1.getMaGv().equals(jTextField6.getText())){
                    userName= jTextField6.getText();
                    gv= gv1;
                    break;
                }
            }
        }
          if(userName.equals("")){
            loadData();
        }
          else{
            DefaultTableModel dtmgv= new DefaultTableModel();
            dtmgv.addColumn("Mã giáo vụ");
            dtmgv.addColumn("Họ tên GV");
            dtmgv.addColumn("Năm sinh"); 
            dtmgv.addColumn("Địa chỉ");
            dtmgv.addRow(new Object[]{gv.getMaGv(), gv.getTenGv(), gv.getNamSinh(), gv.getDiaChi()});
            jTable1.setModel(dtmgv);  
          }   
    }//GEN-LAST:event_jTextField6KeyPressed
        
