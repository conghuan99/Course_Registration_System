    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Giaovu gv= new Giaovu();
        gv.setMaGv(jTextField1.getText());
        gv.setPassword(jTextField1.getText());
        gv.setTenGv(jTextField2.getText());
        try {
            gv.setNamSinh(new SimpleDateFormat("yyyy-mm-dd").parse(jTextField3.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(g_qlgv_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        gv.setDiaChi(jTextField4.getText());
        gv.setSdt(jTextField5.getText());
        
        if(GiaovuDAO.themGiaoVu(gv)){
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            loadData();
        }
        else{
             JOptionPane.showMessageDialog(null, "Thêm thất bại!");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String maGV= (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        Giaovu gv= GiaovuDAO.layThongTinGiaoVu(maGV);
        jTextField1.setText(gv.getMaGv());
        jTextField2.setText(gv.getTenGv());
        jTextField3.setText(gv.getNamSinh().toString());
        jTextField4.setText(gv.getDiaChi());
        jTextField5.setText(gv.getSdt());
    }//GEN-LAST:event_jTable1MouseClicked

